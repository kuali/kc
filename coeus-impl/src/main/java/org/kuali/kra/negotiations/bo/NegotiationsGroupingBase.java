package org.kuali.kra.negotiations.bo;

import org.kuali.coeus.sys.framework.model.KcPersistableBusinessObjectBase;
import org.kuali.rice.core.api.mo.common.active.MutableInactivatable;

public abstract class NegotiationsGroupingBase extends KcPersistableBusinessObjectBase implements Comparable<NegotiationsGroupingBase>, MutableInactivatable {

    private Long id;

    private String code;

    private String description;

    private boolean active;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public boolean isActive() {
        return active;
    }

    public void setActive(boolean active) {
        this.active = active;
    }

    @Override
    public boolean equals(Object o) {
        boolean retVal = false;
        if (o != null && o.getClass().equals(this.getClass())) {
            NegotiationStatus other = (NegotiationStatus) o;
            retVal = this.getId().equals(other.getId());
        }
        return retVal;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((id == null) ? 0 : id.hashCode());
        result = prime * result + ((code == null) ? 0 : code.hashCode());
        result = prime * result + ((description == null) ? 0 : description.hashCode());
        return result;
    }

    /**
     * 
     * This method implements comparable based on the code field.
     * @param o
     * @return
     */
    public int compareTo(NegotiationsGroupingBase o) {
        return this.getCode().compareTo(o.getCode());
    }
}
