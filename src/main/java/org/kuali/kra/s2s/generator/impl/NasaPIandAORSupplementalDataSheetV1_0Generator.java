package org.kuali.kra.s2s.generator.impl;

import gov.grants.apply.forms.nasaPIandAORSupplementalDataSheetV10.FederalAgencyDataType;
import gov.grants.apply.forms.nasaPIandAORSupplementalDataSheetV10.NasaPIandAORSupplementalDataSheetDocument;
import gov.grants.apply.forms.nasaPIandAORSupplementalDataSheetV10.NasaPIandAORSupplementalDataSheetDocument.NasaPIandAORSupplementalDataSheet;
import gov.grants.apply.forms.nasaPIandAORSupplementalDataSheetV10.NasaPIandAORSupplementalDataSheetDocument.NasaPIandAORSupplementalDataSheet.AuthorizedRepresentativeName;
import gov.grants.apply.forms.nasaPIandAORSupplementalDataSheetV10.NasaPIandAORSupplementalDataSheetDocument.NasaPIandAORSupplementalDataSheet.PrincipalInvestigatorName;
import gov.grants.apply.system.globalLibraryV20.HumanNameDataType;
import gov.grants.apply.system.globalLibraryV20.YesNoDataType;

import org.apache.log4j.Logger;
import org.apache.xmlbeans.XmlObject;
import org.kuali.kra.infrastructure.KraServiceLocator;
import org.kuali.kra.proposaldevelopment.bo.ProposalPerson;
import org.kuali.kra.proposaldevelopment.bo.ProposalYnq;
import org.kuali.kra.proposaldevelopment.document.ProposalDevelopmentDocument;
import org.kuali.kra.s2s.S2SException;
import org.kuali.kra.s2s.generator.S2SBaseFormGenerator;
import org.kuali.kra.s2s.generator.bo.DepartmentalPerson;
import org.kuali.kra.s2s.service.S2SBudgetCalculatorService;
import org.kuali.kra.s2s.service.S2SUtilService;
import org.kuali.kra.s2s.util.S2SConstants;

/**
 * 
 * This class is used to generate XML Document object for grants.gov NasaPIandAORSupplementalDataSheetV1.0. This form is generated
 * using XMLBean API's generated by compiling NasaPIandAORSupplementalDataSheetV1.0 schema.
 * 
 * @author Kuali Research Administration Team (kualidev@oncourse.iu.edu)
 */
public class NasaPIandAORSupplementalDataSheetV1_0Generator extends S2SBaseFormGenerator {
    private static final Logger LOG=Logger.getLogger(NasaPIandAORSupplementalDataSheetV1_0Generator.class); 
    private S2SUtilService s2sUtilService;
    private S2SBudgetCalculatorService s2sBudgetCalculatorService;
    private static final String PROPOSAL_YNQ_INTERNATIONAL_PARTICIPATION = "25";
    private static final String PROPOSAL_YNQ_US_GOVERNMENT_PARTICIPATION = "24";

    /**
     * 
     * Constructs a NasaPIandAORSupplementalDataSheetV1_0Generator.java.
     */
    public NasaPIandAORSupplementalDataSheetV1_0Generator() {
        s2sUtilService = KraServiceLocator.getService(S2SUtilService.class);
        s2sBudgetCalculatorService = KraServiceLocator.getService(S2SBudgetCalculatorService.class);
    }

    /**
     * 
     * This method returns NasaPIandAORSupplementalDataSheetDocument object based on proposal development document which contains
     * the NasaPIandAORSupplementalDataSheetDocument informations AuthorizedRepresentativeName, PrincipalInvestigatorName for a
     * particular proposal
     * 
     * @return nasaPIandAORSupplementalDataSheetDocument {@link XmlObject} of type NasaPIandAORSupplementalDataSheetDocument.
     */
    private NasaPIandAORSupplementalDataSheetDocument getnasapIandAORSupplementalDataSheetDocument() {
        NasaPIandAORSupplementalDataSheetDocument nasaPIandAORSupplementalDataSheetDocument = NasaPIandAORSupplementalDataSheetDocument.Factory
                .newInstance();
        NasaPIandAORSupplementalDataSheet nasapIandAORSupplementalDataSheet = NasaPIandAORSupplementalDataSheet.Factory
                .newInstance();
        nasapIandAORSupplementalDataSheet.setFormVersion(S2SConstants.FORMVERSION_1_0);

        nasapIandAORSupplementalDataSheet.setAuthorizedRepresentativeName(getAuthorizedRep());
        nasapIandAORSupplementalDataSheet.setPrincipalInvestigatorName(getPrincipalInvestigator());

        nasaPIandAORSupplementalDataSheetDocument.setNasaPIandAORSupplementalDataSheet(nasapIandAORSupplementalDataSheet);
        return nasaPIandAORSupplementalDataSheetDocument;
    }

    /**
     * 
     * This method returns authorizedRepresentativeName which contains AORName,UserName
     * 
     * @return AuthorizedRepresentativeName object with AORname and user name details.
     */
    private AuthorizedRepresentativeName getAuthorizedRep() {
        AuthorizedRepresentativeName authorizedRepresentativeName = AuthorizedRepresentativeName.Factory.newInstance();

        DepartmentalPerson aorInfo = s2sUtilService.getDepartmentalPerson(pdDoc);
        authorizedRepresentativeName.setAORName(globLibV20Generator.getHumanNameDataType(aorInfo));

        // TODO HardCoded because table EPS_PROP_CUSTOM_DATA was not found in database
        // authorizedRepresentativeName.setUserName("");
        return authorizedRepresentativeName;
    }

    /**
     * 
     * This method returns principalInvestigatorName which contains PDPIName,USGovernmentParticipation,
     * FederalAgency,FederalAgencyDollar,InternationalParticipation.
     * 
     * @return principalInvestigatorName (PrincipalInvestigatorName) object containing principal investigator details.
     */
    private PrincipalInvestigatorName getPrincipalInvestigator() {
        PrincipalInvestigatorName principalInvestigatorName = PrincipalInvestigatorName.Factory.newInstance();

        ProposalPerson PI = s2sUtilService.getPrincipalInvestigator(pdDoc);
        if (PI != null) {
            principalInvestigatorName.setPDPIName(globLibV20Generator.getHumanNameDataType(PI));
        }
        else {
            principalInvestigatorName.setPDPIName(HumanNameDataType.Factory.newInstance());
        }

        // Mandatory fields. So set default values
        principalInvestigatorName.setUSGovernmentParticipation(YesNoDataType.N_NO);
        principalInvestigatorName.setInternationalParticipation(YesNoDataType.N_NO);

        for (ProposalYnq proposalYnq : pdDoc.getProposalYnqs()) {
            if (proposalYnq.getQuestionId() != null && proposalYnq.getQuestionId().equals(PROPOSAL_YNQ_US_GOVERNMENT_PARTICIPATION)) {
                String answer = null;
                String explanation = null;
                if (proposalYnq.getAnswer() != null) {
                    answer = proposalYnq.getAnswer();
                }
                if (proposalYnq.getExplanation() != null) {
                    explanation = proposalYnq.getExplanation();
                }
                FederalAgencyDataType.Enum FederalAgencyEnum = null;
                if (explanation != null) {
                    FederalAgencyEnum = FederalAgencyDataType.Enum.forString(explanation);
                }
                if (answer != null) {
                    if (answer.equals(S2SConstants.PROPOSAL_YNQ_ANSWER_Y)) {
                        principalInvestigatorName.setUSGovernmentParticipation(YesNoDataType.Y_YES);
                        if (FederalAgencyEnum != null) {
                            principalInvestigatorName.setFederalAgency(FederalAgencyEnum);
                        }
                        try {
                            principalInvestigatorName.setFederalAgencyDollar(s2sBudgetCalculatorService.getProposalPersonSalary(pdDoc,
                                    PI).bigDecimalValue());
                        }
                        catch (S2SException e) {
                            LOG.error(e.getMessage(),e);
                        }
                    }
                    else if (answer.equals(S2SConstants.PROPOSAL_YNQ_ANSWER_N)) {
                        principalInvestigatorName.setUSGovernmentParticipation(YesNoDataType.N_NO);
                    }
                }
            }
        }

        for (ProposalYnq proposalYnq : pdDoc.getProposalYnqs()) {
            if (proposalYnq.getQuestionId() != null && proposalYnq.getQuestionId().equals(PROPOSAL_YNQ_INTERNATIONAL_PARTICIPATION)) {
                String answer = null;
                if (proposalYnq.getAnswer() != null) {
                    answer = proposalYnq.getAnswer();
                }
                if (answer != null) {
                    if (answer.equals(S2SConstants.PROPOSAL_YNQ_ANSWER_Y)) {
                        principalInvestigatorName.setInternationalParticipation(YesNoDataType.Y_YES);
                        break;
                    }
                    else if (answer.equals(S2SConstants.PROPOSAL_YNQ_ANSWER_N)) {
                        principalInvestigatorName.setInternationalParticipation(YesNoDataType.N_NO);
                        break;
                    }
                }
            }
        }

        return principalInvestigatorName;
    }

    /**
     * This method creates {@link XmlObject} of type {@link NasaPIandAORSupplementalDataSheetDocument} by populating data from the
     * given {@link ProposalDevelopmentDocument}
     * 
     * @param proposalDevelopmentDocument for which the {@link XmlObject} needs to be created
     * @return {@link XmlObject} which is generated using the given {@link ProposalDevelopmentDocument}
     * @see org.kuali.kra.s2s.generator.S2SFormGenerator#getFormObject(ProposalDevelopmentDocument)
     */
    public XmlObject getFormObject(ProposalDevelopmentDocument proposalDevelopmentDocument) {
        this.pdDoc = proposalDevelopmentDocument;
        return getnasapIandAORSupplementalDataSheetDocument();
    }

    /**
     * This method typecasts the given {@link XmlObject} to the required generator type and returns back the document of that
     * generator type.
     * 
     * @param xmlObject which needs to be converted to the document type of the required generator
     * @return {@link XmlObject} document of the required generator type
     * @see org.kuali.kra.s2s.generator.S2SFormGenerator#getFormObject(XmlObject)
     */
    public XmlObject getFormObject(XmlObject xmlObject) {
        NasaPIandAORSupplementalDataSheetDocument nasapIandAORSupplementalDataSheetDocument = NasaPIandAORSupplementalDataSheetDocument.Factory
                .newInstance();
        NasaPIandAORSupplementalDataSheet nasapIandAORSupplementalDataSheet = (NasaPIandAORSupplementalDataSheet) xmlObject;
        nasapIandAORSupplementalDataSheetDocument.setNasaPIandAORSupplementalDataSheet(nasapIandAORSupplementalDataSheet);
        return nasapIandAORSupplementalDataSheetDocument;
    }

}
