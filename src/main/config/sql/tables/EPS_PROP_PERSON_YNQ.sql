 CREATE TABLE EPS_PROP_PERSON_YNQ (
    PROPOSAL_NUMBER         number(12,0)   constraint EPS_PROP_PERSON_YNQ_N1 not null,
    PROP_PERSON_NUMBER      NUMBER(12,0)   constraint EPS_PROP_PERSON_YNQ_N2 NOT NULL ENABLE,
    QUESTION_ID             VARCHAR2(4)    constraint EPS_PROP_PERSON_YNQ_N3 NOT NULL ENABLE, 
    ANSWER                  CHAR(1) NOT NULL,
	UPDATE_TIMESTAMP DATE constraint EPS_PROP_PERSON_YNQ_N4 NOT NULL ENABLE,
	UPDATE_USER VARCHAR2(8) constraint EPS_PROP_PERSON_YNQ_N5 NOT NULL ENABLE,
	VER_NBR NUMBER(8,0) DEFAULT 1  constraint  EPS_PROP_PERSON_YNQ_N6  NOT NULL ENABLE,
	OBJ_ID VARCHAR2(36) DEFAULT SYS_GUID() NOT NULL,
    CONSTRAINT EPS_PROP_PERSON_YNQ_C0 UNIQUE (OBJ_ID) NOT DEFERRABLE INITIALLY IMMEDIATE,
	CONSTRAINT EPS_PROP_PERSON_YNQ_P1 PRIMARY KEY (PROPOSAL_NUMBER, PROP_PERSON_NUMBER) ENABLE
);
