-- View for Coeus compatibility 
CREATE OR REPLACE VIEW OSP$PROPOSAL_SPECIAL_REVIEW(
    PROPOSAL_NUMBER,
    SEQUENCE_NUMBER,
    SPECIAL_REVIEW_NUMBER,
    SPECIAL_REVIEW_CODE,
    APPROVAL_TYPE_CODE,
    PROTOCOL_NUMBER,
    APPLICATION_DATE,
    APPROVAL_DATE,
    COMMENTS,
    UPDATE_USER,
    UPDATE_TIMESTAMP)
AS SELECT 
    B.PROPOSAL_NUMBER,
    B.SEQUENCE_NUMBER,
    A.SPECIAL_REVIEW_NUMBER, 
    A.SPECIAL_REVIEW_CODE, 
    A.APPROVAL_TYPE_CODE, 
    A.PROTOCOL_NUMBER, 
    A.APPLICATION_DATE, 
    A.APPROVAL_DATE, 
    A.COMMENTS, 
    A.UPDATE_USER, 
    A.UPDATE_TIMESTAMP
FROM 
    PROPOSAL_SPECIAL_REVIEW A, PROPOSAL B
WHERE 
    A.PROPOSAL_ID = B.PROPOSAL_ID;