DECLARE
      notsys_prnplc_id VARCHAR2(40);
BEGIN
       BEGIN
            -- THIS IS PART OF RICE BOOTSTRAP AND HENCE SHOULD BE PRESENT IN OUR DB ALREADY
            SELECT PRNCPL_ID INTO notsys_prnplc_id FROM KRIM_PRNCPL_T WHERE UPPER(PRNCPL_NM) = 'NOTSYS';
       EXCEPTION
         WHEN NO_DATA_FOUND THEN 
              INSERT INTO KRIM_ENTITY_T (ACTV_IND,ENTITY_ID,LAST_UPDT_DT,OBJ_ID,VER_NBR) VALUES ('Y','1131',SYSDATE,SYS_GUID(),1);
              INSERT INTO KRIM_PRNCPL_T (ACTV_IND,ENTITY_ID,LAST_UPDT_DT,OBJ_ID,PRNCPL_ID,PRNCPL_NM,VER_NBR) VALUES ('Y','1131', SYSDATE,SYS_GUID(),'notsys','notsys',1);
              INSERT INTO KRIM_ENTITY_ENT_TYP_T(ENTITY_ID, ENT_TYP_CD, ACTV_IND, LAST_UPDT_DT, VER_NBR, OBJ_ID) VALUES ((SELECT ENTITY_ID FROM KRIM_PRNCPL_T WHERE UPPER(PRNCPL_NM) = 'NOTSYS'), 'PERSON', 'Y', SYSDATE, 1, SYS_GUID());
       END;
     
     COMMIT;
END; 
