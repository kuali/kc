CREATE OR REPLACE VIEW OSP$PROTOCOL_RISK_LEVELS AS SELECT 
    PROTOCOL_NUMBER, 
    SEQUENCE_NUMBER, 
    RISK_LEVEL_CODE, 
    COMMENTS, 
    DATE_ASSIGNED, 
    DATE_INACTIVATED AS DATE_UPDATED, 
    STATUS, 
    UPDATE_USER,
    UPDATE_TIMESTAMP
FROM PROTOCOL_RISK_LEVELS;                          