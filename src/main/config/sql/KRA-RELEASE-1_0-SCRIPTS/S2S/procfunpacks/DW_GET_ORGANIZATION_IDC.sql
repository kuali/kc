CREATE OR REPLACE PROCEDURE          DW_GET_ORGANIZATION_IDC 
   ( as_organization_id IN OSP$ORGANIZATION_IDC.organization_id%TYPE,
     cur_idc IN OUT result_sets.cur_generic) is

begin
open cur_idc for
SELECT OSP$ORGANIZATION_IDC.ORGANIZATION_ID,
	 OSP$ORGANIZATION_IDC.IDC_NUMBER,
         OSP$ORGANIZATION_IDC.START_DATE,
         OSP$ORGANIZATION_IDC.END_DATE,
	 OSP$ORGANIZATION_IDC.REQUESTED_DATE,
         OSP$ORGANIZATION_IDC.IDC_RATE_TYPE_CODE,
	 OSP$ORGANIZATION_IDC.APPLICABLE_IDC_RATE,
	 OSP$ORGANIZATION_IDC.IDC_COMMENT,
         OSP$ORGANIZATION_IDC.UPDATE_TIMESTAMP,
         OSP$ORGANIZATION_IDC.UPDATE_USER
FROM 	OSP$ORGANIZATION_IDC
WHERE	OSP$ORGANIZATION_IDC.ORGANIZATION_ID = as_organization_id;
end;
/

